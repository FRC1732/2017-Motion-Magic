package org.usfirst.frc.team1732.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {

    public final int LEFT_JOYSTICK_USB = 2;
    public final int RIGHT_JOYSTICK_USB = 1;

    public final int PCM_CAN_ID = 0;

    public final int LEFT_MASTER_MOTOR_DEVICE_NUMBER = 5;
    public final int LEFT_1_MOTOR_DEVICE_NUMBER = 6;
    public final int LEFT_2_MOTOR_DEVICE_NUMBER = 7;

    public final int RIGHT_MASTER_MOTOR_DEVICE_NUMBER = 0;
    public final int RIGHT_1_MOTOR_DEVICE_NUMBER = 1;
    public final int RIGHT_2_MOTOR_DEVICE_NUMBER = 2;

    public final int DRIVE_TRAIN_SHIFTER_SOLENOID_DEVICE_NUMBER = 0;

}
